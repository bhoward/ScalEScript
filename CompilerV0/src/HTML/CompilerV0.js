function ifThen(test, trueClause) {
    if (test) {
        return trueClause;
    }
}
function ifThenElse(test, trueClause, falseClause) {
    if (test) {
        return trueClause;
    } else {
        return falseClause;
    }
}
function whileLoop(test, body) {
    while (test) {
        body;
    }
}

